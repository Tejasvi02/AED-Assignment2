    private void txtseatNoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtseatNoKeyPressed
        // TODO add your handling code here:
                
        char modelNumKeyPressed = evt.getKeyChar();
        if(Character.isLetter(modelNumKeyPressed)){
            txtseatNo.setEditable(false);
            lblvaliseat.setText("Please enter number Only!");
        }
        else
        {
            txtseatNo.setEditable(true);
            lblvaliseat.setText("");
        }
    }//GEN-LAST:event_txtseatNoKeyPressed

