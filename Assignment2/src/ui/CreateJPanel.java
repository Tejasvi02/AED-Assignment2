    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        int seatNo= Integer.parseInt(txtseatNo.getText());
        int manufactureYear=Integer.parseInt(txtManufactureYear.getText());
        String modelNo=txtModelNo.getText();
        String city=txtCity.getText();
        String serialNo=txtSerialNo.getText();
        int maintananceCert=Integer.parseInt(txtMaintanenceCert.getText());
        String manufacturer=txtManufacturer.getText();
        boolean availability=chkAvailability.isSelected();
        String company=txtcompany.getText();
     
        
        Uber u=history.addNewUbers();
        
        u.setSeatNo(seatNo);
        u.setMaintananceCert(maintananceCert);
        u.setModelNo(modelNo);
        u.setCity(city);
        u.setSerialNo(serialNo);
        u.setManufactureYear(manufactureYear);
        u.setManufacturer(manufacturer);
        u.setAvailability(availability);
        u.setcompany(company);
        
        
        JOptionPane.showMessageDialog(this,"New Car added.");
        
        txtseatNo.setText("");
        txtManufactureYear.setText("");
        txtModelNo.setText("");
        txtCity.setText("");
        txtSerialNo.setText("");
        txtMaintanenceCert.setText(""); 
        txtManufacturer.setText("");
        chkAvailability.isSelected();
        txtcompany.setText("");
        
    }//GEN-LAST:event_btnsaveActionPerformed

